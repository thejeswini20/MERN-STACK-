//Arithematic operator(+,-,*,/,%)
var a = 10;
var b = "20";
console.log(a+b);
console.log(a-b);
console.log(a*b);
console.log(a/b);
console.log(a%b);

//Relational Operator(>,<,>=,<=,==,!=,===)
var a= 10;
var b="10";
console.log(a>b);
console.log(a<b);
console.log(a>=b);
console.log(a<=b);
console.log(a==b);
console.log(a!=b);
console.log(a===b);
console.log(a!==b);

//Assignment Operator
a = 10;
console.log(a);
a+=10;
console.log(a);
a-=10;
console.log(a);
a*=2;
console.log(a);
a/=2;
console.log(a);
a%=2;
console.log(a);

//Logical Operator(&&,||,!)
a = true;
b = false;
console.log(a&&b);
console.log(a||b);
console.log(!a);

